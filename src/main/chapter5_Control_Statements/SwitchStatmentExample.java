package main.chapter5_Control_Statements;

public class SwitchStatmentExample {

    public static void main(String[] args) {

        String str = "switch";

        switch (str) {
            case "hello":
                System.out.println("hello");
                break;
            case "switch":
                System.out.println("hello");
                break;
                default:
                    System.out.println("Default");

        }
    }
}
